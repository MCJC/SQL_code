SELECT
/*---------------------------------------------------------------------------------------------------------------------------------------------------*/
       distinct
       [DetRel1]
      ,[DetRel2]
      ,[Religion_group_pk]
      ,[misDetRel]
      ,[matched]
from
(
SELECT
/*---------------------------------------------------------------------------------------------------------------------------------------------------*/
       ID
      ,[DetRel1]
      ,[DetRel2]             = [DetRel]
      ,[Religion_group_pk]
      ,[misDetRel]
      ,[matched]             = case when [DetRel1]  = [DetRel] then 'same'
                                    when [DetRel1] != [DetRel] then 'diff'
                                    else                             '!!!!'
                                end
-------------------------------------------------------------------------------------------------------------------------------------------------------
  FROM
(
SELECT 
       distinct
       ID = [SVY_psraid] 
      ,[DetRel1] = [DetRel]
  FROM
      (
                  SELECT * FROM [xDataLoaded].[dbo].[microdata_RLS1cUS]
        --UNION ALL SELECT * FROM [xDataLoaded].[dbo].[microdata_RLS1AnH]
      )                                                        RLS
) microd
-------------------------------------------------------------------------------------------------------------------------------------------------------
--LEFT
JOIN
-------------------------------------------------------------------------------------------------------------------------------------------------------
       [Pew_Religion_Group]
-------------------------------------------------------------------------------------------------------------------------------------------------------
   ON
       CASE 
       WHEN [DetRel1] = 'Don’t know/refused (no information on religious affiliation)'
       THEN            'Don''t know/refused (no information on religious affiliation)'
       WHEN [DetRel1] = 'Jehovah’s Witness'
       THEN            'Jehovah''s Witness'
       WHEN [DetRel1] = 'Church of God Cleveland Tennessee'
       THEN            'Church of God Cleveland, Tennessee'
       ELSE [DetRel1]
        END
    =  [Pew_religion]
-------------------------------------------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------------------------------------------

join
       [xDataLoaded].[dbo].[microdata_RLS_ID]
   ON
       [ID]
    =  [SVY_psraid]
) D35K

order by
[misDetRel] desc, 
[DetRel1]