/***************************************************************************************************************************************************************/
ALTER  VIEW
                       [dbo].[vm__RegionalMigration_IN&OUT&NET_2010to2015]
AS
/***************************************************************************************************************************************************************/
WITH RegMigr --(SalesPersonID, NumberOfOrders, MostRecentOrderDate)
            AS
              (
				SELECT
						[Field_fk]
					  , [Year]                  =   '2010-2015'
					  , [Scenario_id]
					  , [Region_of_Origin]
					  , [Region_of_Destination]
					  , [Religion_group_fk]
					  , [Pew_RelL02_Display]    = CASE
                                                  WHEN    [Pew_RelL02_Display]
                                                        = 'Other Religions'
                                                  THEN    'Members of '
                                                        + [Pew_RelL02_Display]
                                                  ELSE    [Pew_RelL02_Display]
                                                  END
					  , [Total Migrants]
					  , [Display]
				FROM
				(
				SELECT
						[Field_fk]
					  , [Scenario_id]
					  , [Region_of_Origin]      = O.[SubRegion6]
					  , [Region_of_Destination] = D.[SubRegion6]
					  , [Religion_group_fk]
					  , [Pew_RelL02_Display]
					  , [Total Migrants]        = SUM([Migrant_Count])
					  , [Display]               = MIN([Display_by_Religion])
				  FROM [vm__Migration_Flow_by_Country]
					 , [Pew_Religion_Group]
					 , [Pew_Nation]                        O
					 , [Pew_Nation]                        D
				WHERE
					     [Scenario_id]
					 =    4
				AND
						 [Religion_group_pk]
					 =   [Religion_group_fk]
				AND
					   O.[Nation_pk]
					 =   [Origin_Nation_fk]
				AND
					   D.[Nation_pk]
					 =   [Destination_Nation_fk]
				AND
					   D.[Nation_pk]
					 =   [Destination_Nation_fk]
				GROUP BY 
						  [Field_fk]
					  ,   [Scenario_id]
					  , O.[SubRegion6]
					  , D.[SubRegion6]
					  ,   [Religion_group_fk]
					  ,   [Pew_RelL02_Display]
				)                                                                  B
				JOIN
					  [Pew_Field]
				  ON
						 [Field_pk]
					 =   [Field_fk]
                WHERE
                      [Field_year] IN 
                         (   '2010-2015' )
					                                                     )
/***************************************************************************************************************************************************************/
/***************************************************************************************************************************************************************/
SELECT
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
        [v_row]      = 
                         ROW_NUMBER()OVER(ORDER BY
						    ALLO.[Year]
						  , ALLO.[Scenario_id]
						  , ALLO.[Region]
						  , ALLO.[Religion]                )
      , ALLO.[Year]
      , ALLO.[Scenario_id]
      , ALLO.[Region]
      , ALLO.[Religion]
      ,      [Immigrants] = ROUND(([Immigrants] ), 0) 
      ,      [Emigrants]  = ROUND(([Emigrants]  ), 0) 
      ,      [Mill_NET]   = ROUND(([Immigrants]
                                  -[Emigrants]  ), 0) 
      --,      [Mill_NET]   = CAST(ROUND(([Immigrants]
      --                                 -[Emigrants]  ), 0) AS DEC(8,2))
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
FROM
/***************************************************************************************************************************************************************/
(
/***************************************************************************************************************************************************************/
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
        [Year]
      , [Scenario_id]
      , [Region]     = [Region_of_Origin]
      , [Religion]   = [Pew_RelL02_Display]
      , [S1]         = [Religion_group_fk]
      , [Emigrants]  = SUM([Total Migrants]) -- /1000000
FROM
        [RegMigr]
WHERE
        [Region_of_Origin]
     != [Region_of_Destination]
GROUP BY 
        [Year]
      , [Scenario_id]
      , [Religion_group_fk]
      , [Pew_RelL02_Display]
      , [Region_of_Origin]
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
/***************************************************************************************************************************************************************/
)                                                                                                                    ALLO
/***************************************************************************************************************************************************************/
,
/***************************************************************************************************************************************************************/
(
/***************************************************************************************************************************************************************/
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
        [Year]
      , [Scenario_id]
      , [Region]     = [Region_of_Destination]
      , [Religion]   = [Pew_RelL02_Display]
      , [S1]         = [Religion_group_fk]
      , [Immigrants] = SUM([Total Migrants]) -- /1000000
FROM
        [RegMigr]
WHERE
        [Region_of_Origin]
     != [Region_of_Destination]
GROUP BY 
        [Year]
      , [Scenario_id]
      , [Religion_group_fk]
      , [Pew_RelL02_Display]
      , [Region_of_Destination]
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
/***************************************************************************************************************************************************************/
)                                                                                                                    ALLD
/***************************************************************************************************************************************************************/
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
WHERE
        ALLO.[Year]
      = ALLD.[Year]
  AND
        ALLO.[Scenario_id]
      = ALLD.[Scenario_id]
  AND
        ALLO.[Region]
      = ALLD.[Region]
  AND
        ALLO.[Religion]
      = ALLD.[Religion]
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
/***************************************************************************************************************************************************************/
/***************************************************************************************************************************************************************/
GO
