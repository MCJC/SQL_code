ALTER  VIEW
               [dbo].[vi_SurveyQuestions_ByYear]
AS
/***************************************************************************************************************************************************************/
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT 
         [SQYv_row]
          = ROW_NUMBER()OVER(ORDER BY
         [Question_abbreviation_std]
       , [Question_Year]                     )
       , [Question_abbreviation_std]
       , [Question_short_wording_std]
       , [Question_Year]
  FROM 
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
       (  SELECT 
                 DISTINCT
                 [Question_abbreviation_std]
               , [Question_short_wording_std]
               , [Question_Year]
            FROM [vi_Survey_Tables_Displayable]  ) T
-----------------------------------------------------------------------------------------------------------------------------------------------------------------
/***************************************************************************************************************************************************************/
GO
