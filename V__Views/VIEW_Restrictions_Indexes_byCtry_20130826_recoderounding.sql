ALTER VIEW    [dbo].[RIndexes]
AS
/***  ALL INDEXES  ***********************************************************************************************************************/
SELECT 
       [Q_Level] = 'Index'
      ,[Nation_fk]
      ,[Ctry_EditorialName] = i.[Ctry_EditorialName]
      ,[Question_Year]
      ,[Question_abbreviation_std]
      ,[Question_short_wording_std]
      ,[Answer_value] = [Value]
      ,[Region]
  FROM
/*****************************************************************************************************************************************/
       [forum].[dbo].[V_Pew_LongIndex]       i
/*****************************************************************************************************************************************/
      ,[forum].[dbo].[Pew_Question_Std]
/*****************************************************************************************************************************************/
      ,[forum].[dbo].[Pew_Nation]
/*****************************************************************************************************************************************/
WHERE 
       [Variable]
     = [Question_abbreviation_std]
AND
       [Nation_fk]
     = [Nation_pk]
AND
       [Question_abbreviation_std]
    IN 
       (
          'GFI'
        , 'GRI'
        , 'SHI'
                 )
